// formatting input
let data = input.split("\n");

// general algorithm is to store "start" characters, "end" characters should be the last item in the start, otherwise they will be a syntax error
let errors = [];
const startChars = ["{", "(", "[", "<"];
const endChars = ["}", ")", "]", ">"];
data.forEach(line => {
    let known = [];
    let complete = false;
    line.split("").forEach(char => {
        if (complete) return;
        if (startChars.includes(char)) return known.push(char);
        if (known[known.length - 1] == startChars[endChars.indexOf(char)]) {
            known.pop();
        } else {
            errors.push(char);
            complete = true;
        }
    });
});

// calculates score of errors
let pointsGiven = {
    ")": 3,
    "]": 57,
    "}": 1197,
    ">": 25137
}
let sum = 0;
errors.forEach(char => sum += pointsGiven[char]);
