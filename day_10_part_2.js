// formatting input
let data = input.split("\n");

// find lines with errors, discard them and store start characters required for lines that do not error in newData
let required = []
const startChars = ["{", "(", "[", "<"];
const endChars = ["}", ")", "]", ">"];
const remove = [];
data.forEach((line, index) => {
    let known = [];
    let error = false;
    line.split("").forEach(char => { // perhaps use for loop instead for optimisation, but I'm lazy
        if (error) return;
        if (startChars.includes(char)) return known.push(char);
        if (known[known.length - 1] == startChars[endChars.indexOf(char)]) {
            known.pop();
        } else {
            remove.push(index);
            error = true;
        }
    });
    if (!error) required.push(known);
});

// calculates score of errors
let pointsGiven = {
    "(": 1,
    "[": 2,
    "{": 3,
    "<": 4
}
let totals = [];
required.forEach(list => {
    let total = 0;
    list.reverse().forEach(char => {
        total = total * 5 + pointsGiven[char];
    });
    totals.push(total);
});
